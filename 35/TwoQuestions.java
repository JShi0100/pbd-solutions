import java.util.Scanner;

public class TwoQuestions {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        
        System.out.println("Think of something and I'll try to guess it!");
    
        System.out.print("Does it belong inside or outside or both? ");
        String live = input.next();
            
        System.out.print("Is it a living thing? ");
        String answer = input.next();
        
        if (live.equals("inside") && answer.equals("yes")) {
            System.out.println("What else could you be thinking about, other than a house plant?!");
            
        } if (live.equals("both") && answer.equals("yes")) {
            System.out.println("Hmm... I think that you're thinking of a puppy?");
            
        } if (live.equals("outside") && answer.equals("yes")) {
            System.out.println("You're thinking about a grasshopper aren't you?");
            
        } if (live.equals("inside") && answer.equals("no")) {
            System.out.println("Oh! That's easy, it's a shower curtain!");
            
        } if (live.equals("outside") && answer.equals("no")) {
            System.out.println("A doormat of course!");
            
        } if (live.equals("both") && answer.equals("no")) {
            System.out.println("That's too easy, it's a shoe!");
        }
    }
}               
